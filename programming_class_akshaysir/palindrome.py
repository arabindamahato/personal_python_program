''' By checking reverse number and main number if the number is same then it is palindrome'''

print('To reverse the given no')
n=(input('Enter your no : '))
m=int(n)
o=n[::-1]
p=int(o)
if m==p:
    print(' palindrome')
else:
    print(' not a Palindrome')

